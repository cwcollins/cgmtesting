    /**
     * Change display based on combo box selection
     */
    public void changeDisplay() {
        int index = testTypesBox.getSelectedIndex();
        //System.out.println(index);
        if(index == 3) {
            showButtons();
            keyboardTesting = false;
        }
        else {
            hideButtons();
            keyboardTesting = true;
        }
    }
    
    /**
     * Hides all the buttons
     */
