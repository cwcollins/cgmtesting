/**
 * When a key is typed perform an action
 * @param e the key event
 */
public class TestKeyDispatcher implements KeyEventDispatcher {
        @Override
        public boolean dispatchKeyEvent(KeyEvent e) {
            
            if(keyboardTesting) {
                System.out.println("Key code: " + e.getKeyCode());
                if(e.getKeyCode() == 32) {
                    setup();
                    return true;
                }
            }
            else {
                System.out.println("Not listening");
            }
            return false;
        }
}

/**
 * Countdown timer before tests begin
 */
public void countdown() {
    infoLabel.setText("Starting test in 5 seconds...");
    timer = new Timer();
    timer.schedule(new CountdownTask(), 5000);
}

class CountdownTask extends TimerTask {
    public void run() {
        Toolkit.getDefaultToolkit().beep();
        timer.cancel();}
 }

